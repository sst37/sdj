
public class Person
{
   private String name;
   private String birthday;

   public void setName(String na)
   {
      name = na;   
   }
   public void setBirthday(String bday)
   {
      birthday = bday;
   }
   public String getName()
   {
      return name;
   }

   public String getBirthday()
   {
      return birthday;
   }
}
